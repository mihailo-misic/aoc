func hasWon(board [][]int, nums []int) bool {
	if hasWonHori(board, nums) {
		return true
	}
	if hasWonVerti(board, nums) {
		return true
	}

	return false
}

func hasWonHori(board [][]int, nums []int) bool {
	for _, row := range board {
		if hasAllNums(row, nums) {
			return true
		}
	}
	return false
}

func hasWonVerti(board [][]int, nums []int) bool {
	for i := range board[0] {
		col := []int{}
		for _, row := range board {
			col = append(col, row[i])
		}
		if hasAllNums(col, nums) {
			return true
		}
	}
	return false
}

func hasAllNums(sl []int, nums []int) bool {
	for _, n := range sl {
		if !includes(nums, n) {
			return false
		}
	}
	return true
}

func includes(s []int, e int) bool {
	for _, a := range s {
		if a == e {
			return true
		}
	}
	return false
}

